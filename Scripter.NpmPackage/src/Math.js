/**
 * Subset of the JavaScript Math object. Uses the Unity Mathf implementations.
 */
export class Math {
    /**
     * @param {number} value
     * @returns {number}
     */
    abs(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    acos(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    asin(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    atan(value) {}

    /**
     * @param {number} y
     * @param {number} x
     * @returns {number}
     */
    atan2(y, x) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    ceil(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    cos(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    exp(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    floor(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    log(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    log10(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    max(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    min(value) {}

    /**
     * @param {number} x
     * @param {number} y
     * @returns {number}
     */
    pow(x, y) {}

    /**
     * Returns a random number between 0 and 1. Uses the Unity Random.value implementation.
     * @returns {number}
     */
    random() {}

    /**
     * @param {number} value
     * @returns {number}
     */
    round(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    sign(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    sin(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    sqrt(value) {}

    /**
     * @param {number} value
     * @returns {number}
     */
    tan(value) {}
}